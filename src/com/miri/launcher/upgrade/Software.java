package com.miri.launcher.upgrade;

import java.io.Serializable;

/**
 * @author penglin
 * @version TVLAUNCHER001, 2013-6-15
 */
public class Software implements Serializable {

    private static final long serialVersionUID = 5734719607614575578L;

    String version;

    Integer versionCode;

    String publicDate;

    String lastBuildDate;

    String url;

    String force;

    String log;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getForce() {
        return force;
    }

    public void setForce(String force) {
        this.force = force;
    }

    public String getLog() {
        return log;
    }

    public void setLog(String log) {
        this.log = log;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public Integer getVersionCode() {
        return versionCode;
    }

    public void setVersionCode(Integer versionCode) {
        this.versionCode = versionCode;
    }

    public String getPublicDate() {
        return publicDate;
    }

    public void setPublicDate(String publicDate) {
        this.publicDate = publicDate;
    }

    public String getLastBuildDate() {
        return lastBuildDate;
    }

    public void setLastBuildDate(String lastBuildDate) {
        this.lastBuildDate = lastBuildDate;
    }

    @Override
    public String toString() {
        return "Software [version=" + version + ", versionCode=" + versionCode + ", publicDate="
                + publicDate + ", lastBuildDate=" + lastBuildDate + ", url=" + url + ", force="
                + force + ", log=" + log + "]";
    }

}
