package com.miri.launcher.msg.model;

import java.io.Serializable;

import com.google.gson.annotations.SerializedName;

/**
 * @author penglin
 * @version TVLAUNCHER001, 2013-6-15
 */
public class Servers implements Serializable {

    private static final long serialVersionUID = 5678871164553636401L;

    @SerializedName("imageServer")
    String imgServer;

    @SerializedName("advertServer")
    String adServer;

    @SerializedName("upgradeServer")
    String upgradeServer;

    public String getImgServer() {
        return imgServer;
    }

    public void setImgServer(String imgServer) {
        this.imgServer = imgServer;
    }

    public String getAdServer() {
        return adServer;
    }

    public void setAdServer(String adServer) {
        this.adServer = adServer;
    }

    public String getUpgradeServer() {
        return upgradeServer;
    }

    public void setUpgradeServer(String upgradeServer) {
        this.upgradeServer = upgradeServer;
    }

    @Override
    public String toString() {
        return "ServerInfo [imgServer=" + imgServer + ", adServer=" + adServer + ", upgradeServer="
                + upgradeServer + "]";
    }

}
