package com.miri.launcher.msg.model.doc;

import java.io.Serializable;

import com.google.gson.annotations.SerializedName;

/**
 * @author penglin
 * @version TVLAUNCHER001, 2013-6-15
 */
public class BaseDocument implements Serializable {

    private static final long serialVersionUID = -1435797921747392914L;

    @SerializedName("Version")
    String version;

    @SerializedName("PublicDate")
    String publicDate;

    @SerializedName("LastBuildDate")
    String lastBuildDate;

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getPublicDate() {
        return publicDate;
    }

    public void setPublicDate(String publicDate) {
        this.publicDate = publicDate;
    }

    public String getLastBuildDate() {
        return lastBuildDate;
    }

    public void setLastBuildDate(String lastBuildDate) {
        this.lastBuildDate = lastBuildDate;
    }

    @Override
    public String toString() {
        return "Base [version=" + version + ", publicDate=" + publicDate + ", lastBuildDate="
                + lastBuildDate + "]";
    }

}
